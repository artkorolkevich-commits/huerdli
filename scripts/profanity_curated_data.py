"""Проверенные матерные существительные 5–6 букв (только кириллица)."""

import re

MIN_PROFANITY_WORDS = 160
MIN_PROFANITY_5 = 100  # при нехватке 6-буквенных опираемся на 5-буквенные

_CYRILLIC = re.compile(r"^[а-яё]+$")


def _validate_word(word: str) -> str:
    w = word.strip().lower().replace("ё", "е")
    if not _CYRILLIC.match(word):
        raise ValueError(f"Слово не на кириллице: {word!r}")
    if any(ord(c) < 128 for c in word):
        raise ValueError(f"Латиница в слове: {word!r}")
    if len(w) not in (5, 6):
        raise ValueError(f"Длина не 5–6: {word!r} ({len(w)})")
    return w


