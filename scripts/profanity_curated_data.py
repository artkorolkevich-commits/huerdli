def _validate_word(word: str) -> str:
    if not passes_profanity_rules(word):
        reason = reject_reason_profanity(word) or "не проходит правила"
        raise ValueError(f"{word!r}: {reason}")
    return word.strip().lower().replace("ё", "е")


